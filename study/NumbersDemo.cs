public class NumbersDemo{
    public void print(){
        Console.WriteLine(Math.Abs(-10)); // Absolute value
        Console.WriteLine(Math.Ceiling(10.5)); // Round up
        Console.WriteLine(Math.Floor(10.5)); // Round down
        Console.WriteLine(Math.Max(10, 20)); // Maximum of two numbers
        Console.WriteLine(Math.Min(10, 20)); // Minimum of two numbers
        Console.WriteLine(Math.Pow(2, 3)); // Power of a number (2^3)
        Console.WriteLine(Math.Sqrt(16)); // Square root of a number
        Console.WriteLine(Math.Round(10.5)); // Round to nearest integer
        Console.WriteLine(Math.PI); // Value of Pi
    }
}